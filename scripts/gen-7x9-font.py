# Note that this font is laid out differently than the 5x8 font --
# this font is linear.
# However, a line from ROM is accessed from address (character line, character pointer)
# so line n of character code c is stored at address c * 9 + n on the ROM.
# [NOTE: A line of 5x8 font is accessed as (character pointer, character line)]

